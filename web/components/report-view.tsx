"use client";

/**
 * ReportView renders a ReportDoc as an "executive document": serif body,
 * narrow column, sky-500 accents only on H1 underline. Same component is
 * used for the on-screen preview and the standalone HTML download — the
 * download path simply serializes the rendered DOM with inline styles.
 */

import ReactMarkdown from "react-markdown";
import remarkGfm from "remark-gfm";
import type { ReportDoc, ReportSection } from "@/lib/api";

const SNIPPET_LABEL: Record<string, string> = {
  analysis: "Analysis code",
  sql: "SQL",
  chart: "Chart code",
};

function Section({ index, section }: { index: number; section: ReportSection }) {
  return (
    <section className="report-section">
      <h2>
        <span className="report-section-number">{index}.</span>{" "}
        {section.question || "Finding"}
      </h2>
      <div className="report-body">
        <ReactMarkdown remarkPlugins={[remarkGfm]}>{section.bodyMd}</ReactMarkdown>
      </div>
      {section.charts.map((c) => (
        <figure key={c.id} className="report-figure">
          {/* eslint-disable-next-line @next/next/no-img-element */}
          <img src={c.dataUrl} alt={c.title || "chart"} />
          {c.title ? <figcaption>{c.title}</figcaption> : null}
        </figure>
      ))}
      {section.snippets.length > 0 ? (
        <details className="report-snippets">
          <summary>Methodology · code used to compute this finding</summary>
          {section.snippets.map((s) => (
            <div key={s.id} className="report-snippet">
              <p className="report-snippet-label">
                {SNIPPET_LABEL[s.type] ?? s.type}
              </p>
              <pre>
                <code>{s.code}</code>
              </pre>
            </div>
          ))}
        </details>
      ) : null}
    </section>
  );
}

export function ReportView({
  report,
  title,
  subtitle,
  author,
}: {
  report: ReportDoc;
  title: string;
  subtitle?: string;
  author?: string;
}) {
  const formattedDate = new Date(report.generatedAt).toLocaleDateString(
    undefined,
    { year: "numeric", month: "long", day: "numeric" },
  );
  return (
    <article className="report-doc">
      <header className="report-header">
        <h1>{title}</h1>
        {subtitle ? <p className="report-subtitle">{subtitle}</p> : null}
        <p className="report-meta">
          {author ? `${author} · ` : ""}
          {formattedDate} · Dataset:{" "}
          <span className="report-dataset">{report.datasetName}</span>{" "}
          ({report.datasetMeta.rowCount.toLocaleString()} rows ·{" "}
          {report.datasetMeta.columns.length} columns)
        </p>
      </header>
      {report.sections.length === 0 ? (
        <p className="report-empty">
          No findings pinned. Pin at least one assistant turn in the chat to
          include it here.
        </p>
      ) : (
        report.sections.map((s, i) => (
          <Section key={s.id} index={i + 1} section={s} />
        ))
      )}
      <footer className="report-footer">
        <p>Generated by Data Analyst</p>
      </footer>
    </article>
  );
}

