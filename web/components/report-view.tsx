/** Build a standalone HTML document string from a rendered report root. */
export function reportToStandaloneHTML(
  rootHTML: string,
  title: string,
): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<title>${escapeHtml(title)}</title>
<meta name="generator" content="Data Analyst">
<style>${REPORT_STYLES}</style>
</head>
<body>${rootHTML}</body>
</html>`;
}

function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}
